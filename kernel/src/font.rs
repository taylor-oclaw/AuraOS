pub fn draw_char(buf: &mut [u32], stride: usize, x: usize, y: usize, ch: char, color: u32) {
    let idx = match ch {
        '0'..='9' => ch as usize - '0' as usize,
        'A'..='Z' => ch as usize - 'A' as usize + 10,
        _ => return, // Unsupported character
    };

    for (i, byte) in FONT_DATA[idx].iter().enumerate() {
        let row = y + i;
        if row >= buf.len() / stride {
            break;
        }
        for j in 0..FONT_WIDTH {
            let col = x + j;
            if col < stride && (byte & (1 << (7 - j))) != 0 {
                buf[row * stride + col] = color;
            }
        }
    }
}

pub fn draw_string(buf: &mut [u32], stride: usize, mut x: usize, y: usize, s: &str, color: u32) {
    for ch in s.chars() {
        draw_char(buf, stride, x, y, ch, color);
        x += FONT_WIDTH;
    }
}